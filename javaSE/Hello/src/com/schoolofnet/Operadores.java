package com.schoolofnet;

public class Operadores {
	public static void main(String[] args) {
		int a = 15;
		int b = 35;
		int c = a + b;
		int d = a - b;
		int e = a * b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
		// + para somar
		// - para subtrair
		// * para multiplicar
		// / para dividir
		// % para pegar o resto da divisão
		// ++ para incrementar
		// -- para decrementar
		// == comparativo se são iguais
		// != comparativo se são diferentes
	}
}